// Arrays are variables that can hold more than one value.
// that with arrays, we can store multiple values in a single variable.

// //  example: 
// 1. how to declare an array:
const array = [1, 2, 3, 4, 5];
console.log(array);

// output:
// [ 1, 2, 3, 4, 5 ]

// 2. how to access an array element:
console.log(array[0]); // 1
console.log(array[1]); // 2
console.log(array[2]); // 3



// 3. how to change an array element:
array[0] = 10;
